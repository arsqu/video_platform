<template>
  <div id="anchor">
    <a-form-model :model="orderForm" :layout="'inline'">
      <a-form-model-item>
        <a-select
          allowClear
          style="width:120px"
          v-model="orderForm.condition.condition.result"
          :placeholder="mWords('result', 's')"
        >
          <a-select-option
            v-for="(item, idx) in $t('mapConfig.audit.result')"
            :key="idx"
            :value="idx"
            >{{ item }}</a-select-option
          >
        </a-select>
      </a-form-model-item>
      <a-form-model-item>
        <a-date-picker
          show-time
          v-model="orderForm.condition.startTime"
          :placeholder="mWords('startTime', 's')"
        />
      </a-form-model-item>
      <a-form-model-item>
        <a-date-picker
          show-time
          v-model="orderForm.condition.endTime"
          :placeholder="mWords('endTime', 's')"
        />
      </a-form-model-item>
      <a-form-model-item>
        <a-button
          type="primary"
          :loading="loading"
          @click="
            () => {
              this.submit()
            }
          "
        >
          <a-icon type="search"></a-icon>查询
        </a-button>
      </a-form-model-item>
    </a-form-model>
    <!-- x: 1300, -->
    <simpleTable
      class="listView"
      :size="'small'"
      :bordered="false"
      :expand="expand"
      :scroll="{ y: sHet }"
      :columns="columns"
      :loading="loading"
      :tableData="tableData"
      :pagination="pagination"
      :change="handleTableChange"
    />
  </div>
</template>
<script>
export default {
  created() {},
  mounted() {
    this.sHet = document.documentElement.clientHeight - 440 + 48
    // 二级菜单
    // var showKey = ['imgUrl', 'voiveUrl', 'videoUrl']
    // this.expand.table.columns = this.columns.filter(res => {
    //   if (showKey.indexOf(res.dataIndex) !== -1) {
    //     return res
    //   }
    // })
  },
  data() {
    return {
      expand: {
        table: { columns: [] }
      },
      content: '',
      tableData: [],
      pageParam: {
        page: 1,
        pageSize: 10
      },
      loading: false,
      sHet: null,
      formData: {},
      pagination: {
        // '2', 测试用
        pageSizeOptions: ['10', '30', '50', '100'],
        showTotal: total => {
          return `Total ${total} items`
        },
        showSizeChanger: true,
        showQuickJumper: true
      },
      mapConfig: {},
      orderForm: {
        condition: {
          timeName: 'createTime',
          orderBy: 'createTime',
          asc: '0',
          startTime: null,
          endTime: null,
          condition: {
            result: undefined
          }
        }
      },
      columns: [
        {
          title: _ => this.mWords('goldNum', 't'),
          dataIndex: 'goldNum'
        },
        {
          title: _ => this.mWords('amount', 't'),
          dataIndex: 'amount'
        },
        {
          title: _ => this.mWords('handFee', 't'),
          dataIndex: 'handFee'
        },
        {
          title: _ => this.mWords('bardInfo', 't'),
          dataIndex: 'bardInfo'
        },
        {
          title: _ => this.mWords('content', 't'),
          dataIndex: 'content'
        },
        {
          title: _ => this.mWords('result', 't'),
          dataIndex: 'result',
          customRender: v => {
            var tree = {
              0: 'error',
              1: 'success',
              2: 'default'
            }
            return (
              <div>
                <a-badge
                  status={tree[v]}
                  text={this.$t('mapConfig.audit.result')[v]}
                />
              </div>
            )
          }
        },
        {
          width: 150,
          title: _ => this.mWords('createTime', 't'),
          dataIndex: 'createTime',
          sorter: true,
          defaultSortOrder: 'ascend', // 默认升序
          customRender: v => {
            return moment(v).format('YYYY/MM/DD HH:mm:ss')
          }
        },
        {
          width: 150,
          title: _ => this.mWords('updateTime', 't'),
          dataIndex: 'updateTime',
          customRender: v => {
            return moment(v).format('YYYY/MM/DD HH:mm:ss')
          }
        },
        {
          width: 80,
          title: _ => this.$t('tableList.header.operation'),
          dataIndex: '',
          fixed: 'right',
          customRender: (v, d) => {
            if (d.result === 2) {
              return (
                <div>
                  <a-button
                    size="small"
                    type="primary"
                    onClick={v => {
                      this.audit(d, 1)
                    }}
                  >
                    通过
                  </a-button>
                  <a-button
                    class="mt-1"
                    size="small"
                    type="danger"
                    onClick={v => {
                      // 拒绝
                      this.$Modal.confirm({
                        title: '请输入拒绝的原因',
                        maskClosable: true,
                        content: h => (
                          <a-input v-model={this.content}></a-input>
                        ),
                        onOk: close => {
                          if (!this.content) {
                            this.$util.Message('warning', '请输入拒绝的原因')
                            return
                          }
                          var o = Object.assign({}, d)
                          o.content = this.content
                          this.audit(o, 0, close)
                        },
                        onCancel() {
                          console.log('取消')
                        }
                      })
                      console.log('拒绝')
                    }}
                  >
                    拒绝
                  </a-button>
                </div>
              )
            } else {
              return ''
            }
          }
        }
      ]
    }
  },
  methods: {
    // 审批
    audit(d, v, close) {
      console.log(d, v)
      var param = {
        id: d.id,
        uid: d.uid,
        content: d.content,
        goldNum: d.goldNum,
        result: v
      }
      this.$axios
        .postData('audit.withdraw.allot', param)
        .then(res => {
          if (res.code === 200) {
            if (res.data) {
              console.log('操作成功')
              this.submit()
              if (typeof close === 'function') {
                close()
              }
              this.$util.Message('success', this.$t('tips.success.def'))
            } else {
              console.log('操作失败')
              this.$util.Message('error', this.$t('tips.error.def'))
            }
          }
        })
        .catch(err => {
          this.loading = false
          console.log(err)
        })
    },
    createEl(v) {
      v = v ? v.split(',') : []
      return v.map(item => {
        return (
          <div class="over_ellipsis" title={item}>
            <a target="_blank" href={item}>
              {item}
            </a>
          </div>
        )
      })
    },
    handleTableChange(pagination, filters, sorter) {
      const pager = { ...this.pagination }
      pager.current = pagination.current // 当前页
      var sort = {
        ascend: 0,
        descend: 1
      }
      if (sorter.field && sorter.order) {
        sort = sort[sorter.order]
      } else {
        sort = 0
      }
      this.pagination = pager
      this.orderForm.condition.asc = sort
      this.submit({ page: pagination.current, pageSize: pagination.pageSize })
    },
    mWords(key, type) {
      var map = {
        s: 'searchForm',
        t: 'defTable',
        f: 'defForm'
      }
      return this.$t('audit.withdraw.' + map[type] + '.' + key)
    },
    setDefDate() {
      var origin = this.orderForm.condition
      var sT = origin.startTime
      var sE = origin.endTime
      if (!sT && !sE) {
        sT = moment().startOf('day')
        sE = moment().endOf('day')
      } else if (!sT && sE) {
        // 只有结束时间
        sT = moment(sE).startOf('day')
      } else if (sT && !sE) {
        // 只有开始时间
        sE = moment(sT).endOf('day')
      } else {
      }
      origin.startTime = sT
      origin.endTime = sE
      return { sT, sE }
    },
    submit(param) {
      var v = {}
      // 默认时间
      var sDate = this.setDefDate()
      var { sT, sE } = sDate
      // v = JSON.parse(JSON.stringify(this.orderForm)) // 克隆数据
      v = this.$util.loopMap({}, this.orderForm)
      v.condition.startTime = sT.valueOf()
      v.condition.endTime = sE.valueOf()
      // v = this.loopMap({}, v) // 剔除值为空的参数
      v = Object.assign({}, param || this.pageParam, v)
      this.loading = true
      this.$axios
        .postData('audit.withdraw.query', v)
        .then(res => {
          var data = res.data
          this.loading = false
          if (res.code === 200) {
            var pagination = { ...this.pagination }
            pagination.total = data.total
            pagination.pageSize = data.size
            this.tableData = data.records
            if (!param) {
              pagination.current = 1
            }
            this.pagination = pagination
          }
          // console.log(res)
        })
        .catch(err => {
          this.loading = false
          console.log(err)
        })
    }
  }
}
</script>
